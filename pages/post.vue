      text-6
      transition
      space-y-4
      class="left-86% top-70%"
      :style="`opacity: ${extendedListOpacity}`"
    >
      <button
        v-for="item in extendedList"
        cursor-pointer
        type="button"
        @click="item.method"
      >
        <Icon :name="item.label" />

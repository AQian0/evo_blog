<template>
  <div ref="postRef" flex="~ col" items-center>
    <div fixed left-0 top-0 z-1 h-40 w-full bg-gradient-to-t evo-post-mask />
    <div mt-24 indent-2 text-10 tracking-0.5rem>{{ post?.title }}</div>
    <div relative my-6 w-192 text-5 class="content">
